#include <iostream>
using namespace std;

int main()
{
    int num1, num2;
    cout<<"Please Enter both the numbers: ";    /* '<<' is called as insertion operater*/
    cin>>num1>>num2;                            /* '>>' is called as extraction operater*/

    cout<<"The sum of both the numbers are: "<<num1 + num2;

    return 0;
}